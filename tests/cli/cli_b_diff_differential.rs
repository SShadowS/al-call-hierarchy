const VERSION_OVERRIDE: &str = "cli-b-v1";

fn repo_root() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
}

/// Vendored fixture tree root (Task 3.3).
fn fixtures_dir() -> PathBuf {
    repo_root().join("tests").join("fixtures")
}

/// In-repo home for the cli-b diff goldens (inputs + outputs), vendored/
/// regenerated by Task 3.3.
fn goldens_dir() -> PathBuf {
    repo_root().join("tests").join("cli-b-goldens").join("diff")
}

fn snapshots_dir() -> PathBuf {
    goldens_dir().join("snapshots")
}

/// Whether `REGEN_TEMP_GOLDENS=1` is set — Rust-owned baselines are
/// regenerated from THIS engine, never copied from al-sem (retired). Delegates
/// to the shared value-gated helper (Task T0.6) rather than checking presence.
fn regen_mode() -> bool {
    regen::regen_mode()
}

fn load_snap(name: &str) -> CborValue {
    let path = snapshots_dir().join(name);
    let bytes = std::fs::read(&path).unwrap_or_else(|e| panic!("read {}: {e}", path.display()));
    deserialize_snapshot(&bytes, Some(SnapshotFormat::Json))
        .unwrap_or_else(|e| panic!("deserialize {}: {e}", path.display()))
}

fn read_golden(name: &str) -> String {
    let path = goldens_dir().join(name);
    std::fs::read_to_string(&path).unwrap_or_else(|e| panic!("read golden {}: {e}", path.display()))
}

fn assert_bytes_eq(label: &str, golden: &str, actual: &str) {
    if golden.as_bytes() != actual.as_bytes() {
        let first = golden
            .bytes()
            .zip(actual.bytes())
            .position(|(g, a)| g != a)
            .unwrap_or(golden.len().min(actual.len()));
        let ctx_g = &golden[first.saturating_sub(40)..(first + 40).min(golden.len())];
        let ctx_a = &actual[first.saturating_sub(40)..(first + 40).min(actual.len())];
        panic!(
            "{label}: BYTE MISMATCH at offset {first}\n  golden len={} actual len={}\n  golden …{ctx_g}…\n  actual …{ctx_a}…",
            golden.len(),
            actual.len()
        );
    }
}

/// Regen-or-check an OUTPUT golden: in `REGEN_TEMP_GOLDENS` mode write `actual`
/// (Rust-owned baseline, regenerated from THIS engine); otherwise byte-compare
/// against the committed in-repo golden.
fn check_or_regen_bytes(name: &str, actual: &str) {
    let path = goldens_dir().join(name);
    if regen_mode() {
        std::fs::create_dir_all(path.parent().expect("golden has a parent dir"))
            .unwrap_or_else(|e| panic!("regen mkdir for {}: {e}", path.display()));
        std::fs::write(&path, actual)
            .unwrap_or_else(|e| panic!("regen write {}: {e}", path.display()));
        eprintln!("REGEN cli-b diff golden: {}", path.display());
        return;
    }
    let golden = std::fs::read_to_string(&path)
        .unwrap_or_else(|e| panic!("read golden {}: {e}", path.display()));
    assert_bytes_eq(name, &golden, actual);
}

