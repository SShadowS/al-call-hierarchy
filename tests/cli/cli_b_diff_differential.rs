/// CBOR / cbor.gz round-trip oracle over a REAL committed snapshot: load the JSON
/// snapshot, re-encode it to CBOR (B0) and cbor.gz, decode both back, and assert
/// the diff engine produces a byte-identical json golden from the round-tripped
/// trees. This locks the deserializer's CBOR + gunzip paths against real snapshot
/// data (the committed inputs are JSON, so CBOR is otherwise corpus-invisible).
#[test]
fn cbor_and_gz_roundtrip_drives_identical_diff() {
    if regen_mode() {
        // This test only VALIDATES round-trip parity against the golden that
        // `diff_pairs_human_json_sarif_byte_match` owns/regenerates; it never
        // writes goldens itself, so it has nothing to do in regen mode.
        eprintln!(
            "cbor_and_gz_roundtrip_drives_identical_diff: skipped in REGEN_TEMP_GOLDENS mode"
        );
        return;
    }
    use al_call_hierarchy::engine::l5::snapshot_full::{serialize_cbor, serialize_cbor_gz};

    let empty: Vec<CborValue> = Vec::new();
    let pair = "abi-signature-changed";
    let old_json = load_snap(&format!("{pair}-old.snap.json"));
    let new_json = load_snap(&format!("{pair}-new.snap.json"));

    // JSON → tree → CBOR → decode → tree' ; and via cbor.gz.
    let old_cbor = serialize_cbor(&old_json);
    let new_cbor = serialize_cbor(&new_json);
    let old_from_cbor =
        deserialize_snapshot(&old_cbor, Some(SnapshotFormat::Cbor)).expect("decode cbor old");
    let new_from_cbor =
        deserialize_snapshot(&new_cbor, Some(SnapshotFormat::Cbor)).expect("decode cbor new");

    let old_gz = serialize_cbor_gz(&old_json);
    let new_gz = serialize_cbor_gz(&new_json);
    let old_from_gz =
        deserialize_snapshot(&old_gz, Some(SnapshotFormat::CborGz)).expect("decode gz old");
    let new_from_gz =
        deserialize_snapshot(&new_gz, Some(SnapshotFormat::CborGz)).expect("decode gz new");

    let golden = read_golden(&format!("{pair}.json"));

    for (label, o, n) in [
        ("cbor", &old_from_cbor, &new_from_cbor),
        ("cbor.gz", &old_from_gz, &new_from_gz),
    ] {
        let result = run_engine(o, n, CoveragePolicy::Loose);
        let json = format_diff(&result, "json", VERSION_OVERRIDE, true, &empty);
        assert_bytes_eq(&format!("{pair}.json (via {label})"), &golden, &json);
    }

    // Auto-detect path (no hint): gz magic 1f 8b, cbor magic b9.
    let auto_gz = deserialize_snapshot(&old_gz, None).expect("auto gz");
    let auto_cbor = deserialize_snapshot(&old_cbor, None).expect("auto cbor");
    let r1 = run_engine(&auto_gz, &new_from_gz, CoveragePolicy::Loose);
    let r2 = run_engine(&auto_cbor, &new_from_cbor, CoveragePolicy::Loose);
    assert_bytes_eq(
        &format!("{pair}.json (auto gz)"),
        &golden,
        &format_diff(&r1, "json", VERSION_OVERRIDE, true, &empty),
    );
    assert_bytes_eq(
        &format!("{pair}.json (auto cbor)"),
        &golden,
        &format_diff(&r2, "json", VERSION_OVERRIDE, true, &empty),
    );
}

// ===========================================================================
// Synthetic oracles — lock the corpus-invisible branches (preflight fatals, the
// higher-severity finding kinds, the human zero-findings-with-diagnostics
// early-exit). Each builds a minimal CborValue snapshot tree by hand and runs the
// engine, asserting the TS-exact behavior.
// ===========================================================================

use indexmap::IndexMap;

/// Build a CborValue::Map from (key, value) pairs.
fn obj(pairs: Vec<(&str, CborValue)>) -> CborValue {
    let mut m: IndexMap<String, CborValue> = IndexMap::new();
    for (k, v) in pairs {
        m.insert(k.to_string(), v);
    }
    CborValue::Map(m)
}

fn txt(s: &str) -> CborValue {
    CborValue::Text(s.to_string())
}

/// A minimal snapshot with a given set of top-level fact arrays. `schemaVersion`
/// defaults to 3 and `apps` to a single app guid `appguid-1` unless overridden.
fn snapshot(extra: Vec<(&str, CborValue)>) -> CborValue {
    let mut pairs: Vec<(&str, CborValue)> = vec![
        ("schemaVersion", CborValue::Int(3)),
        ("alsemVersion", txt("cli-b-v1")),
        (
            "apps",
            CborValue::Array(vec![obj(vec![("appGuid", txt("appguid-1"))])]),
        ),
        (
            "identities",
            obj(vec![
                ("stableIds", CborValue::Array(vec![])),
                ("displayNames", CborValue::Array(vec![])),
            ]),
        ),
    ];
    pairs.extend(extra);
    obj(pairs)
}

fn engine(
    old: &CborValue,
    new: &CborValue,
    policy: CoveragePolicy,
) -> al_call_hierarchy::engine::gate::diff::DiffEngineResult {
    run_diff_engine(
        old,
        new,
        &DiffEngineOptions {
            coverage_policy: policy,
            deterministic: true,
            rename_overlay: None,
        },
    )
}

fn find_kind<'a>(
    r: &'a al_call_hierarchy::engine::gate::diff::DiffEngineResult,
    kind: &str,
) -> &'a al_call_hierarchy::engine::gate::diff::DiffFinding {
    r.findings
        .iter()
        .find(|f| f.kind.as_str() == kind)
        .unwrap_or_else(|| {
            panic!(
                "no finding of kind {kind}; got {:?}",
                r.findings
                    .iter()
                    .map(|f| f.kind.as_str())
                    .collect::<Vec<_>>()
            )
        })
}

/// #5(a) — schemaVersion mismatch → fatal: empty findings + schema-version-mismatch diag.
#[test]
fn oracle_preflight_schema_version_mismatch_is_fatal() {
    let old = snapshot(vec![]);
    let mut new = snapshot(vec![]);
    if let CborValue::Map(m) = &mut new {
        m.insert("schemaVersion".into(), CborValue::Int(3)); // keep new at 3...
    }
    // ...but make OLD differ by mutating its schemaVersion to a sentinel. The
    // deserializer gate rejects ≠3 inputs, but the engine itself must still
    // short-circuit on a mismatch, so we feed the engine directly.
    let mut old_mut = old;
    if let CborValue::Map(m) = &mut old_mut {
        m.insert("schemaVersion".into(), CborValue::Int(99));
    }
    let r = engine(&old_mut, &new, CoveragePolicy::Loose);
    assert!(r.findings.is_empty(), "fatal preflight → no findings");
    assert!(
        r.diagnostics
            .iter()
            .any(|d| d.kind == "schema-version-mismatch")
    );
}

/// #5(b) — alsemVersion mismatch under strict → fatal error diagnostic.
#[test]
fn oracle_preflight_alsem_version_mismatch_strict_is_fatal() {
    let old = snapshot(vec![]);
    let mut new = snapshot(vec![]);
    if let CborValue::Map(m) = &mut new {
        m.insert("alsemVersion".into(), txt("cli-b-v2"));
    }
    let r = engine(&old, &new, CoveragePolicy::Strict);
    assert!(
        r.findings.is_empty(),
        "strict version mismatch → fatal, no findings"
    );
    let diag = r
        .diagnostics
        .iter()
        .find(|d| d.kind == "alsem-version-mismatch")
        .expect("alsem-version-mismatch diag");
    let sev = diag
        .fields
        .iter()
        .find(|(k, _)| k == "severity")
        .map(|(_, v)| v);
    assert!(matches!(sev, Some(CborValue::Text(s)) if s == "error"));

    // Under loose the same mismatch is a WARNING and NOT fatal (findings still flow).
    let r2 = engine(&old, &new, CoveragePolicy::Loose);
    let diag2 = r2
        .diagnostics
        .iter()
        .find(|d| d.kind == "alsem-version-mismatch")
        .expect("alsem-version-mismatch diag (loose)");
    let sev2 = diag2
        .fields
        .iter()
        .find(|(k, _)| k == "severity")
        .map(|(_, v)| v);
    assert!(matches!(sev2, Some(CborValue::Text(s)) if s == "warning"));
}

