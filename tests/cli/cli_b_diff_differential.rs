//! cli-b/b4 — the `diff` CLI differential.
//!
//! For each snapshot pair / variant in the diff manifest, deserialize the
//! vendored snapshot inputs (`tests/cli-b-goldens/diff/snapshots/`), run the
//! Rust diff engine + formatters, and byte-compare every `.human.txt` / `.json` /
//! `.sarif` golden under `tests/cli-b-goldens/diff/`. Also asserts:
//!   - the rename variant (`--renames rename-overlay.json`),
//!   - the strict-coverage exit code (1) + strict json golden,
//!   - the `--fail-on medium` exit code (1),
//!   - the ws-mode stderr note (byte-exact).
//!
//! Ungated: a divergence is a Rust bug to fix, never something to tolerate.
//!
//! Task 3.3 (al-sem parity retirement) vendored every input this file needs
//! (snapshot pairs, `rename-overlay.json`, the `ws-diff-rename` /
//! `ws-diff-removed-field` fixture trees) in-repo and regenerated every OUTPUT
//! golden from THIS engine — no al-sem checkout is read anymore, and there is
//! no skip-gate: missing inputs are a hard failure. Regenerate with
//! `REGEN_TEMP_GOLDENS=1 cargo test --test cli cli_b_diff_differential::`.

